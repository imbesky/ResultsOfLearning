package practice1;

public class VarAndType {

	public static void main(String[] args) {
		int DecimalNum = 10; //10진수 decimal
		int BinaryNum = 0B1010; // 2진수 binary : 앞에 0B
		int OctalNum = 012; // 8진수 Octal : 앞에 0
		int HexadecimalNum = 0XA; // 16진수 hexadecimal : 앞에 0X
		
		System.out.println(DecimalNum);
		System.out.println(BinaryNum);
		System.out.println(OctalNum);
		System.out.println(HexadecimalNum);
	}

}
